/**
 * Static response declarable per endpoint. Returned verbatim when the
 * endpoint uses the `{ response }` form. Mirrors the JSON-safe subset
 * of `MockResponse` (no `text` / `html` / `raw`).
 */
export interface EndpointResponse {
  status: number
  json?: unknown
  empty?: boolean
  headers?: Record<string, string>
}

/**
 * Per-endpoint declaration. Three forms:
 *
 *   - **string**: module path to a handler (shorthand for `{ handler }`)
 *   - **`{ handler, status? }`**: dynamic-import handler. Optional
 *     `status` registers an HTTP status override for this route in
 *     the mock-mode `pathOverrides` map (e.g. force 500 for that route).
 *   - **`{ response }`**: static response only ã no handler module.
 *     The handler is synthesized to return the literal response.
 *
 * `handler` and `response` are mutually exclusive.
 */
export type EndpointSpec =
  | string
  | { handler: string; status?: number; response?: never }
  | { response: EndpointResponse; handler?: never; status?: never }

/**
 * Raw JSON shape declared inside `databehave.json`. Mirrors a
 * narrow subset of `Config` plus the on-disk additions
 * (`endpoints` map of route-key ã `EndpointSpec`).
 */
export interface JsonConfig {
  /** Path to the JSON OpenAPI document, relative to this file. */
  openapi?: string
  /** Server listen options used by the CLI. */
  server?: { host?: string; port?: number }
  /**
   * Prefix automatically prepended to every `endpoints` key.
   * Lets callers write `"GET products/list"` instead of
   * repeating the API prefix in every entry. Must start with `/` and
   * must not end with `/` (e.g. `"/api/v1"`). OpenAPI paths are
   * left untouched ã they already carry their full path in the OAS document.
   */
  basePath?: string
  /**
   * CORS knobs. `origin` may be a single allowed origin string or an
   * allowlist of strings (the function form on `CorsConfig`
   * is reserved for the programmatic API). Anything off the allowlist
   * is rejected (empty header) so misconfigured callers fail loudly
   * instead of silently leaking `*`.
   */
  cors?: Omit<CorsConfig, 'origin'> & {
    origin?: string | readonly string[]
  }
  /** Built-in mock-mode behaviour. Disabled by default. */
  mockMode?: MockModeConfig
  /**
   * Opt-in admin / error-injection panel. Default: disabled.
   * Pass-through to `Config.admin`. The JSON loader
   * automatically supplements `openapiBody` from the resolved OpenAPI
   * document so the UI route picker works without manual wiring.
   */
  admin?: Config['admin']
  /**
   * Opt-in per-request access logs. Pass-through to
   * `Config.log`. Default: disabled. See README
   * "Access logs" for the shape.
   */
  log?: Config['log']
  /**
   * Opt-in inbound request validation against the OpenAPI document.
   * Pass-through to `Config.validation`. Default off
   * (`{ request: false }`) ã when omitted the runtime behaves
   * byte-identical to a build without the validator.
   */
  validation?: Config['validation']
  /**
   * Opt-in auto-schema mode (Phase 2C). When `enabled: true`, every
   * OAS path not covered by `endpoints` is served from data
   * generated by `@databehave/schema`'s `mock()` engine, derived
   * from the OpenAPI document. The peer is loaded via dynamic
   * `import()` at server-creation time; install it separately with
   * `npm i @databehave/schema`. See README "Auto-schema mode".
   *
   * Always an object ã the legacy boolean shorthand
   * (`schema: true`) was removed. Unknown keys throw at
   * config-load time. Fields:
   *
   * - `enabled` ã required boolean. `false` (or omitting the
   *   `schema` field entirely) keeps the default JSONC mock-only
   *   flow.
   * - `seed` ã `number` (fixed), `"stable"` (per-request hash),
   *            or `"random"` (fresh per request). Default `"stable"`.
   * - `locale` ã optional locale tag (forwarded as a future hint).
   * - `arrayCount` ã default array length when OAS lacks `minItems`.
   */
  schema?: {
    enabled: boolean
    seed?: number | 'stable' | 'random'
    locale?: string
    arrayCount?: number
  }
  /**
   * Route-key ã endpoint declaration map. Keys may be written
   * relative to `basePath` (e.g. `"GET products/list"`); they
   * may also be absolute (`"GET /health"`) ã any key starting with
   * `/` after the method is registered verbatim.
   */
  endpoints?: Partial<Record<string, EndpointSpec>>
}

/** Result of loading a JSON config from disk. */
export interface LoadedConfig {
  /** Fully-assembled config ready for `createServer`. */
  readonly config: Config
  /** Server listen options copied verbatim from the JSON. */
  readonly server: { host: string; port: number }
}

