/**
 * JSON / JSONC config loader for @databehave/server.
 *
 * Reads a `databehave.json` (or `.jsonc`) file from disk, validates
 * its shape, resolves the OpenAPI document, dynamically imports every
 * handler referenced by `endpoints`, and wires the built-in mock-mode
 * middleware. The returned value is a regular `Config` ready
 * for `createServer`.
 *
 * JSONC features (`//` comments, `/* block * /` comments, trailing
 * commas) are accepted in any config file regardless of extension.
 *
 * All paths inside the JSON are resolved relative to the JSON file's
 * directory. Handlers must be ESM modules whose `default` export is
 * a `Handler`.
 */
